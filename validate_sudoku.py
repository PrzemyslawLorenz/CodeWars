import math
import pprint


class Sudoku(object):
    def __init__(self, data):
        self.all = []
        for x in range(len(data)):
            newColumn = []
            self.all.append(data[x])
            for y in range(len(data)):
                newColumn.append(data[y][x])
            self.all.append(newColumn)

    def is_valid(self):
        pprint.pprint(self.all)
        for i in self.all:
            for j in i:
                if i.count(j) > 1:
                    return False
        return True


